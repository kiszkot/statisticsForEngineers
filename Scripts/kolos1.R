require(extraDistr)

x = seq(0, 3000, by = 0.01)
y = drayleigh(x, 1000)
q1x = qrayleigh(0.25, 1000)
q1y = drayleigh(q1x, 1000)
q2x = qrayleigh(0.5, 1000)
q2y = drayleigh(q2x, 1000)
q3x = qrayleigh(0.75, 1000)
q3y = drayleigh(q3x, 1000)
domx = 1000
domy = drayleigh(1000, 1000)
ex = 1000*sqrt(pi/2)
ey = drayleigh(ex, 1000)

png("../LaTex/wykresKK.png")
plot(x,y, main = "gestosc", xlab = "t", ylab = "f_T(t)", type = "l", col = "green")
points(q1x, q1y, col = "red")
points(q2x, q2y, col = "red")
points(q3x, q3y, col = "red")
points(domx, domy, col = "blue")
points(ex, ey, col = "black")
legend(x = "topright", legend = c("gestosc","kwartyle","dominanta","EX"), col = c("green","red","blue","black"), lty = 1)
dev.off()